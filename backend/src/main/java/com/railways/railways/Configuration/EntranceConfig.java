    package com.railways.railways.Configuration;

    /**
     * The EntranceConfig class represents a configuration for an entrance in the railway system.
     * It includes details such as the entrance's unique ID and its location in the system (X, Y coordinates).
     */
    public class EntranceConfig {
        // Unique identifier for the entrance
        public int id;
        // X-coordinate of the entrance location
        public int x;
        // Y-coordinate of the entrance location
        public int y;
    }
