package com.railways.railways.logging;

/**
 * Enum representing different levels of logging severity.
 *
 * Each log level corresponds to a different severity of the log message.
 * These levels can be used to control the verbosity of logging and to categorize
 * the importance of log messages.
 */
public enum LogLevel {
    Critical,
    Error,
    Warning,
    Info,
    Debug
}
